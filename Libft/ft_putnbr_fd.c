#include"libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	char	c;
	long	x;

	x = nbr;
	if (x < 0)
	{
		c = '-';
		write (fd, &c, 1);
		x = x * (-1);
	}
	if (x > 9)
	{
		ft_putnbr_fd (x / 10, fd);
		ft_putnbr_fd (x % 10, fd);
	}
	if (x < 10)
	{
		c = x + '0';
		write(fd, &c, 1);
	}
}
